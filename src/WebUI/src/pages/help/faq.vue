<script setup lang="ts">
import { t } from '@/services/translate-service';

const qaCount = 12;
const qaList = computed(() =>
  Array(qaCount)
    .fill(0)
    .map((_, idx) => ({
      q: t(`help.FAQ.list[${idx}].q`),
      a: t(`help.FAQ.list[${idx}].a`),
    }))
    .filter((qa, idx) => qa.q !== `help.FAQ.list[${idx}].q`)
);
</script>

<template>
  <div class="prose prose-invert mx-auto">
    <h2 class="text-center">{{ $t('help.FAQ.title') }}</h2>
    <div class="space-y-10">
      <div class="text-center" v-html="$t('help.FAQ.intro')" />

      <Divider />

      <FormGroup v-for="({ q, a }, idx) in qaList" :label="`${idx + 1}. ${q}`" :collapsable="false">
        <div v-html="a"></div>
      </FormGroup>
    </div>
  </div>
</template>
